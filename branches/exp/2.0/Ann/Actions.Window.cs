namespace Sgry.Ann
{
	static partial class Actions
	{
		/// <summary>
		/// Activate next document.
		/// </summary>
		public static void ActivateNextDocument( AppLogic app )
		{
			app.ActivateNextDocument();
		}

		/// <summary>
		/// Activate previous document.
		/// </summary>
		public static void ActivatePrevDocument( AppLogic app )
		{
			app.ActivatePrevDocument();
		}

		/// <summary>
		/// Shows a dialog listing documents.
		/// </summary>
		public static void ShowDocumentList( AppLogic app )
		{
			app.ShowDocumentList();
		}
	}
}
